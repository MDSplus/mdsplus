MARTE_GENERIC = __import__('MARTE_GENERIC', globals())


class MARTE_WAVEGEN(MARTE_GENERIC.MARTE_GENERIC):
    waveParNames = ['wave01', 'wave02', 'wave03', 'wave04', 'wave05', 'wave06', 'wave07', 'wave08', 'wave09', 'wave10',
                    'wave11', 'wave12', 'wave13', 'wave14', 'wave15', 'wave16', 'wave17', 'wave18', 'wave19', 'wave20',
                    'wave21', 'wave22', 'wave23', 'wave24', 'wave25', 'wave26', 'wave27', 'wave28', 'wave29', 'wave30',
                    'wave31', 'wave32']
    parts = list(MARTE_GENERIC.MARTE_GENERIC.parts)
    parts.append({'path': '.PARAMS', 'type': 'structure'})
    parts.append({'path': '.PARAMS:NUM_ACTIVE', 'type': 'numeric', 'value': 0})

    parts.append({'path': '.WAVE_PARAMS', 'type': 'structure'})
    parts.append({'path': '.WAVE_PARAMS:NUM_ACTIVE',
                  'type': 'numeric', 'value': 32})
    for i in range(len(waveParNames)):
        parts.append(
            {'path': '.WAVE_PARAMS:WAVE_%03d' % (i+1), 'type': 'structure'})
        parts.append(
            {'path': '.WAVE_PARAMS:WAVE_%03d:DESCRIPTION' % (i+1), 'type': 'text'})
        parts.append({'path': '.WAVE_PARAMS:WAVE_%03d:NAME' % (
            i+1), 'type': 'text', 'value': waveParNames[i]})
        parts.append({'path': '.WAVE_PARAMS:WAVE_%03d:X' % (i+1),
                      'type': 'numeric', 'valueExpr': 'Float32Array([0.,1.])'})
        parts.append({'path': '.WAVE_PARAMS:WAVE_%03d:Y' % (i+1),
                      'type': 'numeric', 'valueExpr': 'Float32Array([0.,0.])'})

    for i in range(len(waveParNames), 64):
        parts.append(
            {'path': '.WAVE_PARAMS:WAVE_%03d' % (i+1), 'type': 'structure'})
        parts.append(
            {'path': '.WAVE_PARAMS:WAVE_%03d:DESCRIPTION' % (i+1), 'type': 'text'})
        parts.append(
            {'path': '.WAVE_PARAMS:WAVE_%03d:NAME' % (i+1), 'type': 'text'})
        parts.append(
            {'path': '.WAVE_PARAMS:WAVE_%03d:X' % (i+1), 'type': 'numeric'})
        parts.append(
            {'path': '.WAVE_PARAMS:WAVE_%03d:Y' % (i+1), 'type': 'numeric'})
    del(waveParNames, i)
